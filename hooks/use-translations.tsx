export function useTranslations() {
  const { language } = useLocale()
  const [currentTranslations, setCurrentTranslations] = useState(translations.en)

  useEffect(() => {
    // Set translations based on the selected language
    const langTranslations = translations[language.code] || translations.en
    setCurrentTranslations(langTranslations)
  }, [language.code])

  const t = (key: string, section = "common") => {
    try {
      const parts = key.split(".")

      if (parts.length === 1) {
        // If the key doesn't contain dots, look it up in the specified section
        return currentTranslations[section]?.[key] || key
      } else {
        // If the key contains dots (e.g., "home.heroTitle"), use the first part as section
        const sectionKey = parts[0]
        const translationKey = parts[1]
        return currentTranslations[sectionKey]?.[translationKey] || key
      }
    } catch (error) {
      console.error(`Translation error for key ${key} in section ${section}:`, error)
      return key
    }
  }

  return { t }
}
